package com.hekmatullahamin.offnews.ui.model

/**
 * Represents the different types of news tabs available in the application.
 */
enum class NewsTabType {
    /**
     * The Home tab, displaying the main news feed.
     */
    HOME,

    /**
     * The Bookmark tab, displaying the user's saved articles.
     */
    Bookmark,

    /**
     * The Search tab, allowing users to search for specific articles.
     */
    Search
}